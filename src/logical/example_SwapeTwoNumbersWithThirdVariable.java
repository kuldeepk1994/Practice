package logical;

public class example_SwapeTwoNumbersWithThirdVariable {
public static void main(String[] args) {
	int num1=23;
	int num2=10;
	int temp;
	
	System.out.println("Without Swape Numbers");
	System.out.println(num1);
	System.out.println(num2);
	temp=num1;  // 23
	num1=num2;  //10
	num2=temp; //23
	System.out.println("With Swape Numbers");
	System.out.println(num1);
	System.out.println(num2);
	
}
}
